from ast import Str
from flask_wtf import FlaskForm
from wtforms import StringField, IntegerField, SubmitField, PasswordField, BooleanField
from wtforms.validators import InputRequired, Email, Length

class loginForm(FlaskForm):
    userName = StringField('username', validators=[InputRequired(), Length(min=4, max=15)])
    password = PasswordField('password', validators=[InputRequired(), Length(min=8, max=50)])
    remember = BooleanField('remember me')


class addForm(FlaskForm):
    name = StringField("Student Name: ")
    submit = StringField("Add Student")

class deleteForm(FlaskForm):
    name = StringField("ID No. of Student Name: ")
    submit = StringField("Remove Student")
